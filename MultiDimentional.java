package Arrays;

public class MultiDimentional {
	public static void main(String args[]) {
	int[][] nam={
			{100,200,300},
			{400,500,500},
			{500,800},
			{600,80}
			};
	int sum=0;
	for(int i=0;i<nam.length;i++) {
		for(int j=0;j<nam[i].length;j++) {
	
			System.out.print(nam[i][j]+" ");
	}
	System.out.println();
	}
	}
}
	

	



